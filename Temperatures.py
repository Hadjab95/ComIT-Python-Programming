#This will calculate the sum and average of three environmental temperatures.
temp1 = int(input("Enter the first temperature: "))
temp2 = int(input("Enter the second temperature: "))
temp3 = int(input("Enter the third temperature: "))

sum = temp1 + temp2 + temp3
average = int(temp1+ temp2 + temp3)/3

print(f" The sum of the temperatures is {sum}, and the average temperature is {average}")
